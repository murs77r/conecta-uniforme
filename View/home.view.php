    <h2>Como Funciona</h2>
    
    <h3>Para Responsáveis:</h3>
    <ol>
        <li>Faça login com seu e-mail cadastrado</li>
        <li>Receba um código de acesso temporário por e-mail</li>
        <li>Digite o código para entrar no sistema</li>
        <li>Navegue pelo catálogo personalizado (apenas uniformes da série do seu filho)</li>
        <li>Adicione produtos ao carrinho</li>
        <li>Finalize o pedido para retirada na escola</li>
        <li>Acompanhe o status até estar pronto para retirar</li>
    </ol>
    
    <h3>Para Fornecedores:</h3>
    <ol>
        <li>Acesse com seu e-mail cadastrado</li>
        <li>Cadastre seus produtos com fotos e descrição</li>
        <li>Defina variações (tamanhos, cores, gêneros)</li>
        <li>Homologue produtos para escolas e séries</li>
        <li>Receba e gerencie pedidos</li>
        <li>Atualize status de produção e envio</li>
        <li>Acompanhe relatórios financeiros</li>
    </ol>
    
    <h3>Para Gestores Escolares:</h3>
    <ol>
        <li>Acesse com seu e-mail cadastrado</li>
        <li>Importe lista de alunos (CSV ou individual)</li>
        <li>Habilite fornecedores autorizados</li>
        <li>Monitore pedidos e ponto de coleta</li>
        <li>Gerencie credenciais de responsáveis</li>
    </ol>
    
